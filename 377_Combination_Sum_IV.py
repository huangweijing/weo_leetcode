from typing import List

class Solution:
    def __init__(self):
        self.sol = [-1] * 1001
        self.sol[0] = 1
        self.cnt = 0

    def combinationSum4(self, nums: List[int], target: int) -> int:
        if target < 0:
            return 0

        if self.sol[target] != -1:
            return self.sol[target]

        result = 0
        for num in nums:
            if target - num >= 0:
                result += self.combinationSum4(nums, target - num)

        self.sol[target] = result
        self.cnt += 1
        # print(target, self.sol[target])
        return result

